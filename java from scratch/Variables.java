public class Variables {
    public static void main(String[] args) {
        int num = 0;
        System.out.println(num);
        num = 20;
        System.out.println(num);

        char a = 'm';
        System.out.println(a);

        String s = "Oreo Mcflurry";
        System.out.println(s);

        double d = 2.001;
        System.out.println(d);

    }
}
